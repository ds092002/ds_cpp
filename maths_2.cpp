#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    cout <<"Ceil value is : "<< ceil(7.8) << endl;
    cout <<"Ceil value is : "<< ceil(8.4) << endl;
    cout <<"Floor value is : "<< floor(7.8) << endl;
    cout <<"Floor value is : "<< floor(8.4) << endl;
    cout <<"Round value is : "<< round(4.5) << endl;
    cout <<"Round value is : "<< round(6.2) << endl;
    cout <<"SQRT value is : "<< sqrt(625) << endl;
    cout <<"SQRT value is : "<< sqrt(16) << endl;
    cout <<"POWER value is : "<< pow(5,4) << endl;
    cout <<"POWER value is : "<< pow(8,3) << endl;
    cout <<"ABS value is : "<< abs(30) << endl;
    cout <<"ABS value is : "<< abs(-28) << endl;
}
